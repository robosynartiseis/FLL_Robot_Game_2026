# --------------------------------------------------
# GAME PARTS TABLE (Mission-based)
# --------------------------------------------------
# Λεξικό (dictionary) που περιέχει όλα τα Game Parts
# (αποστολές) του ρομπότ με βάση το Mission ID.

# Σε αντίθεση με τη λίστα (list), εδώ:
# ✔ Δεν χρησιμοποιούμε index (0-based)
# ✔ Χρησιμοποιούμε το πραγματικό Mission ID (1,2,3,...)
# ✔ Επιτρέπει skip αποστολών χωρίς να χαλάει η σειρά

# Αυτό σημαίνει ότι:
# - Αν αφαιρεθεί κάποιο Game Part (π.χ. το 6)
# - Δεν επηρεάζονται τα υπόλοιπα (7,8,9 δουλεύουν κανονικά)

# --------------------------------------------------
# ΔΟΜΗ ΔΕΔΟΜΕΝΩΝ
# --------------------------------------------------
# Κάθε εγγραφή έχει τη μορφή:
# mission_id: (Mission Function, Gear before mission)

# - mission_id:
#   Ο αριθμός της αποστολής (όπως εμφανίζεται στο menu
#   και στην οθόνη του hub)

# - Mission Function:
#   Αναφορά (χωρίς εκτέλεση) στη συνάρτηση που υλοποιεί
#   την αποστολή (π.χ. Run_Mission)
#
#   Σημαντικό:
#   Δεν γράφουμε Run_Mission() αλλά Run_Mission
#   γιατί θέλουμε να την καλέσουμε αργότερα

# - Gear before mission:
#   Ο αριθμός ταχύτητας (gear) που πρέπει να επιλεγεί
#   ΠΡΙΝ ξεκινήσει η αποστολή
#
#   Χρησιμοποιείται για:
#   ✔ προσαρμογή δύναμης / ταχύτητας
#   ✔ καλύτερο έλεγχο μηχανισμών
#   ✔ αποφυγή stalls

# --------------------------------------------------
# ΠΑΡΑΔΕΙΓΜΑ
# --------------------------------------------------
# 1: (Game_Part_01.Run_Mission, 4)
#
# -> Mission ID = 1
# -> Εκτελείται το Game Part 1
# -> Πριν ξεκινήσει:
#    αλλάζει το gearbox στη θέση 4

# --------------------------------------------------
# ΠΛΕΟΝΕΚΤΗΜΑΤΑ ΤΗΣ ΔΟΜΗΣ
# --------------------------------------------------
# ✔ Mission-based execution (όχι index-based)
# ✔ Σταθερή αντιστοίχιση:
#     1 -> Game Part 1 (πάντα)
# ✔ Εύκολο skip αποστολών (enable/disable)
# ✔ Ιδανικό για competition restart
# ✔ Πιο καθαρός και επεκτάσιμος κώδικας

# --------------------------------------------------
# GAME PARTS DEFINITION
# --------------------------------------------------

GAME_PARTS = {
    1: (Game_Part_01.Run_Mission, 4),  # Mission 1 -> Gear 4
    2: (Game_Part_02.Run_Mission, 3),  # Mission 2 -> Gear 3
    3: (Game_Part_03.Run_Mission, 1),  # Mission 3 -> Gear 1
    4: (Game_Part_04.Run_Mission, 1),  # Mission 4 -> Gear 1
    5: (Game_Part_05.Run_Mission, 1),  # Mission 5 -> Gear 1
    6: (Game_Part_06.Run_Mission, 1),  # Mission 6 -> Gear 1
    7: (Game_Part_07.Run_Mission, 2),  # Mission 7 -> Gear 2
    8: (Game_Part_08.Run_Mission, 3),  # Mission 8 -> Gear 3
    9: (Game_Part_09.Run_Mission, 3),  # Mission 9 -> Gear 3
}

def Run_Game_Parts(start_part):
    # --------------------------------------------------
    # Εκτέλεση Game Parts (Mission-based execution)
    # --------------------------------------------------
    # Η συνάρτηση αυτή εκτελεί τις αποστολές του ρομπότ
    # ξεκινώντας από ένα συγκεκριμένο Mission ID (start_part)
    # και συνεχίζοντας μέχρι το τέλος.

    # --------------------------------------------------
    # Ταξινόμηση αποστολών
    # --------------------------------------------------
    # Παίρνουμε όλα τα mission IDs από το GAME_PARTS
    # και τα ταξινομούμε σε αύξουσα σειρά.
    for mission_id in sorted(GAME_PARTS.keys()):

        # --------------------------------------------------
        # Skip αποστολών πριν το start_part
        # --------------------------------------------------
        # Αν το mission είναι μικρότερο από το επιλεγμένο
        # σημαίνει ότι:
        # → Έχουμε κάνει restart από μεταγενέστερο σημείο
        # → Δεν θέλουμε να ξανατρέξουμε προηγούμενες αποστολές
        if mission_id < start_part:
            continue

        # --------------------------------------------------
        # Skip disabled missions
        # --------------------------------------------------
        # Ελέγχουμε αν το συγκεκριμένο mission είναι ενεργό
        # μέσω του robot_state.enabled_missions
        if not robot_state.enabled_missions.get(mission_id, True):
            continue

        # --------------------------------------------------
        # Ανάκτηση δεδομένων αποστολής
        # --------------------------------------------------
        # Από το GAME_PARTS παίρνουμε:
        # - mission: τη συνάρτηση που θα εκτελεστεί
        # - gear: το gear που πρέπει να ενεργοποιηθεί πριν
        mission, gear = GAME_PARTS[mission_id]

        # --------------------------------------------------
        # Εμφάνιση στο Hub
        # --------------------------------------------------
        # Εμφανίζουμε τον αριθμό της αποστολής στην οθόνη
        # για οπτική επιβεβαίωση στον χειριστή
        prime_hub.display.char(str(mission_id))

        # --------------------------------------------------
        # Debug πληροφορίες
        # --------------------------------------------------
        # Αν είναι ενεργό το debug mode:
        # → Εκτυπώνουμε πληροφορίες για την αποστολή
        if robot_state.main_debug_mode:
            print("\n==============================")
            print("Starting Game Part:", mission_id)
            print("==============================")

        # --------------------------------------------------
        # Αλλαγή Gear πριν την αποστολή
        # --------------------------------------------------
        # Εκτελούμε Change_Gear async
        # μέσω run_task για να μην μπλοκάρει το πρόγραμμα
        run_task(Change_Gear(gear))

        # --------------------------------------------------
        # Αναμονή εκκίνησης από χρήστη
        # --------------------------------------------------
        # Περιμένουμε πάτημα κουμπιού πριν ξεκινήσει
        # η αποστολή (χρήσιμο σε competition restart)
        Wait_For_Start()

        # --------------------------------------------------
        # Reset ταχυτήτων / επιταχύνσεων
        # --------------------------------------------------
        # Επαναφέρουμε τις βασικές ρυθμίσεις κίνησης
        # ώστε κάθε αποστολή να ξεκινά σε "καθαρή" κατάσταση
        Initialise_Acceleration_And_Speed()

        # --------------------------------------------------
        # Εκτέλεση αποστολής
        # --------------------------------------------------
        # Καλούμε τη mission async συνάρτηση
        # μέσω run_task
        run_task(mission())

def main():
    # --------------------------------------------------
    # Αρχικοποίηση Hub & ρομπότ
    # --------------------------------------------------
    # Ρυθμίζει:
    # - προσανατολισμό οθόνης
    # - stop buttons
    # - χρήση gyro στο drive base
    # - αρχικές παραμέτρους συστήματος
    Initialise_Hub()

    # --------------------------------------------------
    # Έλεγχος κατάστασης μπαταρίας
    # --------------------------------------------------
    # Εκτυπώνει την τάση και το ρεύμα της μπαταρίας.
    # Χρήσιμο για:
    # - αγωνιστικό έλεγχο πριν την εκκίνηση
    # - εντοπισμό πτώσης απόδοσης λόγω χαμηλής μπαταρίας
    if robot_state.main_debug_mode:
        print('Drive Base -> Voltage:', prime_hub.battery.voltage(), '& Current:', prime_hub.battery.current())

    # --------------------------------------------------
    # Επιλογή Game Part από τον χρήστη (Mission-based)
    # --------------------------------------------------
    # Δημιουργεί δυναμικά ένα menu στην οθόνη του hub,
    # από το οποίο ο χρήστης επιλέγει από ποιο Game Part
    # θέλει να ξεκινήσει.
    
    # --------------------------------------------------
    # Δημιουργία menu_items
    # --------------------------------------------------
    # Η λίστα menu_items περιέχει τα διαθέσιμα missions
    # που θα εμφανιστούν στο menu.
    menu_items = []

    # Διατρέχουμε όλα τα πιθανά Mission IDs (1 έως 9)
    for i in range(1, 10):

        # Ελέγχουμε αν το mission είναι ενεργό
        # μέσω του robot_state.enabled_missions
        #
        # Αν δεν υπάρχει στο dict:
        # → θεωρείται enabled (default True)
        if robot_state.enabled_missions.get(i, True):

            # Προσθέτουμε το mission στο menu
            # ως string (απαραίτητο για το hub_menu)
            menu_items.append(str(i))

    # --------------------------------------------------
    # Εμφάνιση menu στον χρήστη
    # --------------------------------------------------
    selected = hub_menu(*menu_items)

    # --------------------------------------------------
    # Μετατροπή επιλογής σε Mission ID
    # --------------------------------------------------
    # Το μετατρέπουμε σε ακέραιο για χρήση στο πρόγραμμα
    start_part = int(selected)

    # --------------------------------------------------
    # Debug πληροφορίες
    # --------------------------------------------------
    # Αν είναι ενεργό το debug mode:
    # → εμφανίζουμε ποιο mission επέλεξε ο χρήστης
    if robot_state.main_debug_mode:
        print("Selected start Game Part:", start_part)

    # --------------------------------------------------
    # Έλεγχος IMU (Gyroscope)
    # --------------------------------------------------
    # Αν το IMU δεν είναι έτοιμο:
    # - Δεν ξεκινάμε αποστολές
    # - Αποφεύγουμε λάθος στροφές και πορείες
    if not Check_IMU():
        return

    # --------------------------------------------------
    # Εκτέλεση αποστολών
    # --------------------------------------------------
    # Μηδενισμός χρονομέτρου αγώνα
    Robot_Game_Timer.reset()

    # Εκκίνηση των Game Parts από το επιλεγμένο index
    Run_Game_Parts(start_part)

    # --------------------------------------------------
    # Τελικός χρόνος αγώνα
    # --------------------------------------------------
    # Εκτυπώνει τον συνολικό χρόνο που χρειάστηκε
    # το ρομπότ για να εκτελέσει όλες τις αποστολές
    if robot_state.main_debug_mode:
        print('Robot Game -> Duration:', Milliseconds_To_Time(Robot_Game_Timer.time()))

# --------------------------------------------------
# START
# --------------------------------------------------
# Καλεί τη main() και ξεκινά το πρόγραμμα
main()
