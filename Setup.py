# Εισάγουμε τις βιβλιοθήκες που αφορούν τον εξοπλισμό και τις λειτουργίες του ρομπότ

# Για τον έλεγχο και τις λειτουργίες του PrimeHub.
from pybricks.hubs import PrimeHub  

# Παράμετροι που χρησιμοποιούνται για τις λειτουργίες και τον έλεγχο του ρομπότ:
# - Πλευρές (Side), Άξονες (Axis), Πλήκτρα (Button), Κατευθύνσεις (Direction), Θύρες (Port) και Χρώματα (Color).
from pybricks.parameters import Side, Axis, Button, Direction, Port, Color  

# Συσκευές που συνδέονται στο ρομπότ:
# - Αισθητήρες χρώματος (ColorSensor) και μοτέρ (Motor).
from pybricks.pupdevices import ColorSensor, Motor  

# Εργαλεία για τον έλεγχο της κίνησης του ρομπότ, όπως η βάση κίνησης.
from pybricks.robotics import DriveBase  

# Εργαλεία που διευκολύνουν τις λειτουργίες του ρομπότ:
# - Χρονόμετρο (StopWatch), αναμονή (wait), multitasking και εκτέλεση ταυτόχρονων εργασιών.
from pybricks.tools import StopWatch, wait, hub_menu, multitask, run_task

# Η κλάση RobotState περιέχει όλη την κατάσταση (state) του ρομπότ,
# όπως debug modes, gear state, caliper direction και πληροφορίες σφαλμάτων.
# Χρησιμοποιείται για να αποφύγουμε global μεταβλητές και να έχουμε
# κεντρικό έλεγχο της κατάστασης του συστήματος.
from RobotState import RobotState

# Η κλάση GridNavigator υλοποιεί ένα σύστημα εντοπισμού θέσης (localization)
# βασισμένο σε grid (σαν σκακιέρα).
# Χρησιμοποιεί τους δύο αισθητήρες χρώματος για να αναγνωρίζει
# σε ποιο “τετράγωνο” της πίστας βρίσκεται το ρομπότ.
# Η κλάση αυτή αποτελεί το “σύστημα αντίληψης χώρου” του ρομπότ
# και συνεργάζεται με το RobotState για debug και έλεγχο.
from GridNavigator import GridNavigator
#===============================================================================

# Οι παρακάτω σταθερές χρησιμοποιούνται για να διασφαλίσουν ομοιομορφία και 
# να διευκολύνουν τη συντήρηση του κώδικα. Τυχόν αλλαγές μπορούν να γίνουν σε ένα
# μόνο σημείο, επηρεάζοντας όλο τον κώδικα που εξαρτάται από αυτές.

# Δημιουργούμε σταθερές για βασικές παραμέτρους κίνησης
DEFAULT_DRIVE_SPEED = 750  # Προεπιλεγμένη ταχύτητα ευθύγραμμης κίνησης (mm/s).
DEFAULT_DRIVE_ACCELERATION = 1000  # Επιτάχυνση ευθύγραμμης κίνησης (mm/s^2).
DEFAULT_TURN_SPEED = 500  # Προεπιλεγμένη ταχύτητα περιστροφής (μοίρες/s).
DEFAULT_TURN_ACCELERATION = 750  # Επιτάχυνση περιστροφής (μοίρες/s^2).
RESET_WHELLS_TO_WALL_DURATION = 500  # Διάρκεια σε ms για ευθυγράμμιση τροχών με τοίχο.
#===============================================================================

# Δημιουργούμε αντικείμενα για τους αισθητήρες, τα μοτέρ και τη βάση κίνησης
# Αντικείμενο για τον PrimeHub με ρύθμιση κατευθύνσεων:
# Ο άξονας Z δείχνει προς τα πάνω, και ο άξονας Y είναι ο εμπρόσθιος.
prime_hub = PrimeHub(top_side=Axis.Z, front_side=Axis.Y)

# Αισθητήρες χρώματος:
# Ο δεξιός αισθητήρας είναι συνδεδεμένος στη θύρα C και ο αριστερός στη θύρα D.
right_colour_sensor = ColorSensor(Port.C)  # Δεξιός αισθητήρας χρώματος.
left_colour_sensor = ColorSensor(Port.D)  # Αριστερός αισθητήρας χρώματος.

# Χρονόμετρο:
# Χρησιμοποιείται για χρονισμένες ενέργειες και καταγραφή διαρκειών.
Game_Part_Timer = StopWatch()
Robot_Game_Timer = StopWatch()
Function_Timer = StopWatch()

# Μοτέρ για τους τροχούς:
# Το δεξιό μοτέρ είναι συνδεδεμένο στη θύρα B και κινείται δεξιόστροφα.
# Το αριστερό μοτέρ είναι συνδεδεμένο στη θύρα F και κινείται αριστερόστροφα.
right_wheel = Motor(Port.B, Direction.CLOCKWISE)  # Μοτέρ δεξιού τροχού.
left_wheel = Motor(Port.F, Direction.COUNTERCLOCKWISE)  # Μοτέρ αριστερού τροχού.

# Μοτέρ για τις λαβίδες:
# Η δεξιά λαβίδα είναι συνδεδεμένη στη θύρα A και κινείται δεξιόστροφα.
# Η αριστερή λαβίδα είναι συνδεδεμένη στη θύρα E και κινείται δεξιόστροφα.
right_calliper = Motor(Port.A, Direction.CLOCKWISE)  # Δεξιά λαβίδα.
left_calliper = Motor(Port.E, Direction.CLOCKWISE)  # Αριστερή λαβίδα.

# Βάση κίνησης:
# Περιλαμβάνει τα δύο μοτέρ τροχών, με απόσταση τροχών 160mm και διάμετρο τροχού 49.5mm.
drive_base = DriveBase(left_wheel, right_wheel, 49.5, 160)

# Δημιουργία του κεντρικού αντικειμένου κατάστασης του ρομπότ.
# Από εδώ και πέρα:
# - Δεν χρησιμοποιούμε global debug μεταβλητές.
# - Δεν χρησιμοποιούμε global gear_change.
# - Δεν χρησιμοποιούμε global previous_caliper_direction.
# Όλη η κατάσταση αποθηκεύεται μέσα στο robot_state.
# Τα debug flags μπορούν να ενεργοποιούνται ή να απενεργοποιούνται
# από εδώ κεντρικά, επηρεάζοντας όλο το πρόγραμμα.
robot_state = RobotState(
    main_debug=True,
    track_debug=False,
    game_part_debug=False,
    utils_debug=False
)

# Δημιουργία του Grid Navigator (σύστημα εντοπισμού θέσης).
# Το αντικείμενο αυτό επιτρέπει στο ρομπότ να γνωρίζει
# σε ποιο σημείο της πίστας βρίσκεται κάθε στιγμή,
# χρησιμοποιώντας τους δύο αισθητήρες χρώματος.

# Από εδώ και πέρα:
# - Δεν βασιζόμαστε μόνο σε dead reckoning (gyro / distance).
# - Δεν κινείται “τυφλά” το ρομπότ.
# - Έχουμε αναγνώριση θέσης σε grid (σαν σκακιέρα).

# Το grid_navigator:
# - διαβάζει τους αισθητήρες χρώματος
# - αναγνωρίζει patterns (π.χ. green + brown)
# - αντιστοιχίζει τη θέση σε τετράγωνο (π.χ. A1, B2)
# - μπορεί να χρησιμοποιηθεί για navigation & correction

# Συνεργάζεται με:
# - το robot_state για debug πληροφορίες
# - τα missions για έλεγχο θέσης

# Τα thresholds και το grid_map πρέπει να ρυθμιστούν
# με βάση την πραγματική πίστα (calibration).

grid_navigator = GridNavigator(
    left_colour_sensor,
    right_colour_sensor,
    robot_state
)
#===============================================================================
